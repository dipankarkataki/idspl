        <h3>Course Objectives</h3>
      </center>
      <p>
          This course is uniquely curated as an applied learning course on climatology concepts. This will enable the participants to understand climatology through first principles of physics and make the participants confident in appraising research and industrial works. The course was designed by keeping right blend of theory and hands-on tool activities for each lecture to augment understanding. The course will also be complimented by presentations of high-performance building case-studies to better connect theory, learning and practice. The broader objectives are:</p>
          <ol>
              <li>1. Introducing Integrative Design Process (IDS) and its importance</li>
              <li>2. Explaining the fundamentals of building physics through real-time illustrations</li>
              <li>3. Presenting the methods to analyzing the performance of the each and individual building component and system</li>
              <li>4. Teaching energy simulation tool for visual assessment of building performance</li>
          </ol>
<p>There are three levels of course modules:</p>
<ol>
    <li>1. Basic level modules</li>
    <li>2. Intermediate level modules</li>
    <li>3. Advance level modules</li>
