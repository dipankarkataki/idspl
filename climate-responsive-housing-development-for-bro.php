                      <p>BRO project comes under the residential township category, located in the Leh district of
                            UT of Ladakh, and is an altitude of 3,524 meters (11,562 ft). The main intent of this project
                            is to reduce internal energy usage (for heating purposes) through the implementation of
                            passive and active strategies in the buildings. All the pockets segment of the site includes
                            different buildings for married accommodations. The project includes a multi-block (G+1
                            only) residential accommodation for Border Road Organisation’s (BRO) Project Himank, in
                            Choglamsar, Leh. The total built-up area is approx 10,000 sq. ft. This project included an
                            ENS compliance check, building envelope heating simulation analysis, mapping surfacing
                            surface, mapping orientation energy consumption, and coordinating with different
                            stakeholders
                        </p>
                      <p>
                        The project deliverables include-
                        <ul style="list-style-type: circle;">
                            <li>
                                Eco Niwas Samhita 2021 compliance check for 41 residential blocks.
                            </li>
                            <li>
                                Assisting the early design phase developed to ensure less heating load for all the blocks on site.
                            </li>
                            <li>
                                Performed energy simulation for 10 different building envelopes to ensure the right
                                composition for extreme weather conditions. This includes different building materials
                                available and their outside and inside surface temperature analysis.
                            </li>
                            <li>
                                Plotting hourly data for a clear assessment of the envelope performance during different
                                seasons and mapping the energy consumption simultaneously.
                            </li>
                            <li>
                                Coordinating with different stakeholders in the implementation of the energy efficiency
                                measures.
                            </li>
