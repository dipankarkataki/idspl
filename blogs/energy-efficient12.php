                    <p>
                        Since the primary concern while constructing houses and buildings is that they be durable and long-lasting, it makes a lot of environmental sense to pay attention to the materials used. For starters, the operational energy of new buildings has been steadily rising over the years, not to mention the considerable energy required for the manufacture and transportation of building materials. The second consideration is that of embodied energy. At present, the embodied energy used for the construction material is not taken into account. Conventional materials such as bricks, concrete, timber and steel require high amounts of energy for production, leading to increased greenhouse gas emissions. For instance, the production of 1 ton of cement produces 1 ton of carbon dioxide!
<br><br>
However, the biggest challenge to moving to energy-efficient materials lies in the increase in initial costs, which is a severe deterrent for most buyers, especially in developing countries. The irony lies in the fact that, in the long term, energy-efficient materials offer far greater financial gain.
<br><br>
Sustainable housing principles include using composite building materials, building envelope, solar designs, water conservation, waste reduction and recycling and landscape and shading.
<br><br>
<strong>Composite Building Materials</strong><br>
Composite building materials are made up of two or more materials. Natural fiber materials like jute, sisal coconut, ramie, banana offer the dual advantage of being cheap and environment-friendly.
<br><br>
Composite building materials are viable substitutes to traditional materials like timber, steel, aluminium and concrete. Since they are low-weight, they greatly reduce transportation costs, and are also far more resistant to corrosion.
<br><br>
There are many varieties of composite materials, like jute fibre-reinforced polypropylene composites, coir fibre-reinforced composites, sisal fibre and wollastonite jute pultruded composites. There are even composite building materials generated from glass fibres and industrial wastes which can be used to manufacture portable toilets, water storage tanks, sinks, bathtubs, etc.<br>
 <img src="../images/energy-efficient2.png" />
<strong>Building Envelope</strong><br>
The building envelope is the boundary between a building’s conditioned interior and the outdoors. It plays a major role in how much energy the building will require for heating (or cooling), as well as lighting and ventilation. Walls, roofs, and floors are the routes by which heat escapes the building envelope. Proper insulation checks heat loss in winter and prevents overheating in summer. On the flip side, good insulation makes it difficult for moisture to evaporate, resulting in condensation and eventual damage. To counter this problem, air and vapour membranes need to be incorporated into the design. Air barriers resist air movement, and therefore the movement of moisture by increasing airtightness. Vapour barriers allow any moisture that finds itself within the building to escape through diffusion.
<br><br>
 
Windows should be strategically installed to let in as much light as possible, maximizing heat gain in winter, but minimizing it in summer. Such energy balance can be achieved with double or triple glazed well-insulated windows combined with architectural shading.
<br><br>
Reflective surfaces in roof design can help deflect a significant amount of heat in warm climates. This will prevent heat build-up in attics or air-conditioned spaces where it can result in condensation. The simplest way to do this is to specify a white-coloured roof. Such cool roofs have the ability to reflect both visible and infra-red light.
<br><br>
<strong> Solar Design</strong>
Solar design can be active and passive. Passive solar design entails using the naturally available sunlight to heat and cool homes. By keeping the living areas to the north or south depending on which hemisphere a house is located, passive heating can be achieved in cool climatic regions, and vice-versa.
<br><br>
Additionally, thermal mass can be used to store heat energy when it is abundant and release when it is scarce, thereby reducing air-conditioning costs.  The use of concrete slabs or masonry walls can act as a thermal mass.
<br><br>
Active solar designs involve the use of photovoltaic cells to meet the building’s electricity demands. For areas where the amount of sunlight varies greatly on a day-to-day basis, batteries may be installed. (However, PV systems are rather costly compared to conventional systems.) Additionally, solar heaters can be installed to significantly lower heating costs in cold climates.
<br><br>
<strong>Water Conservation</strong>
Water conservation includes installing low-flow fixtures like dual flush toilets, rainwater collection systems that collect roof runoff and greywater reuse.
<br><br>

<strong>Waste Reduction and Recycling</strong>
Waste minimisation can be achieved both during the construction phase and during the operation phase. Use of recycled concrete and other recycled aggregates from demolished buildings, and reduction of on-site waste generation can also be a viable option in makes constructions more environment-friendly.
<br><br>
With the Indian real estate sector slated to grow from Rs 12,000 crore in 2019 to Rs 65,000 crore by 2040, it is time that we change with the times, and that houses of the future – in addition to providing comfort, safety – become resource-efficient and climate-responsive.

