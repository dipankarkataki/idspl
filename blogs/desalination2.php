The recent Chennai water crisis is a wake-up call for India. The irony of a city by the sea facing acute water shortage was not lost on any of the key stakeholders. The NITI Aayog has since announced plans of setting up desalination projects along Indiaâ€™s coastline. Desalination, or the process of turning seawater into drinking water, is being considered as a long-term solution to the drought-like situation across the country, especially during the summers. The process of desalination entails removal of salts and minerals from seawater, making it usable for domestic purposes. India already has around 1000 desalination plants, but none of these are large-scale. The Minjur desalination plant is the largest in the country and has a capacity of 100,000mÂ³/day (100mld). It uses reverse osmosis technology to produce potable water for an estimated population of 500,000 in Chennai. It has been functional since 2010. The government plans to open more desalination plants of varying capacities all across the country. The Jal Shakti Ministry, under the guidance of the NITI Aayog, is in charge of the implementation of these plants. With the new and improved plants, a constant supply of high-quality drinking water will be guaranteed. Incidentally, Israeli Prime Minister Benjamin Netanyahu has gifted a mobile independent desalination van, â€˜Gal-mobileâ€™ to India. The government plans to use it to provide water to the BSF jawans in remote areas. It can purify up to 20,000 litres of water a day. However, if the water is particularly muddy, then only 4,000 litres of water can be purified every day. 
<br><br>
Desalination has already been implemented successfully in countries like Israel, the UAE, the US and Singapore. There are around 18000 desalination plants across the world and they are emerging as a foolproof solution to the worldâ€™s water crisis. Some of these countries, however,  are gradually waking up to the negative aspects of desalination. For instance, It was recently discovered that Israelis consuming desalinated water might be suffering from considerable magnesium deficiency. The Israeli government aims to solve this issue by adding magnesium to the water, after it has been processed. In the long term, more such issues are bound to crop up but they will have to be solved individually instead of envisioning an alternate solution. These countries are not blessed with abundant water resources and desalination is their most lucrative solution in their scenario.
India, on the other hand, is fortunate to have one of the largest number of rivers in Asia. These rivers, however have been rendered unfit for domestic use due to a faulty waste disposal system. A resolution to divert waste from the rivers could generate more potable water than any desalination plant. Another drawback of desalination plants is that they have a very high energy consumption, which in turn, contributes to green-house emissions. These projects could also wreak havoc on marine life along Indiaâ€™s coasts. Desalination results in residual brine. Brine is concentrated salt water which has a very low oxygen content and can destroy marine life. To avoid this, the proposed desalination plants will be built deep into the ocean as floating installations. The NITI Aayog has further clarified that the proposed desalination plants will use recyclable energy, making them ecologically sustainable. The most crucial criticism however, is that these plants are not only expensive, they could be used by private entities as profit-making ventures, hence undoing the entire purpose of such undertakings. The NITI Aayog proposal for setting up desalination plants does not cap the cost of these projects and this loophole could easily be exploited. The maintenance of these plants in the face of frequent cyclones could also add to the cost. 
<br><br>
Whether desalination is really the answer to Indiaâ€™s problem is still debatable. It remains to be seen how the NITI Aayog implements its desalination plans. However, going by the success rate of these plants in countries where it has been implemented, it is definitely a solution worth exploring. While long-term desalination projects might not be the best way forward, it can definitely be an effective interim solution while we lay the groundwork for more sustainable solutions like groundwater recharge and river clean-ups.   
 




