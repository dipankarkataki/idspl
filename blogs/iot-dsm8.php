<b>What is IoT and DSM</b><br><br>
For the starters, we think it is pertinent to give brief introduction of what is Internet of things (IoT) and Demand Side Management (DSM). <br><br>
Draft policy on Internet of things by Department of Electronics & Information Technology (DeitY), Ministry of Communication and Information Technology, Government of India defines IoT as a “seamless connected network system of embedded objects/devices, with identifiers, in which communication without any human intervention by using standard and interoperable communication protocols”. In simple words, IoT, is an environment under which daily-use objects, machines, physical devices, when given the ability to transfer data with or without human interaction over the internet, can interact and communicate amongst each other. These devices can be remotely monitored and controlled over the internet.<br><br> 
   Whereas the Demand Side Management (DSM) is a method of influencing end consumer behaviour and willingness to reduce electricity consumption. DSM initiatives not only help the end customers to reduce their electricity bills but also help utilities in cutting the peak loads, thereby, saving high investment ventures to expand generation, transmission and distribution networks.<br><br>
<b>How IoT Supports DSM</b><br><br>
The rise of e-commerce platforms like Amazon, Flipkart etc. and Reliance Jio ventures are successful business models and looked upon by entrepreneurs. If we look at the micro picture of what turned them into successful models, it is the comfort and affordability. Users now can get their desired product delivered at their home and that too at cheaper costs. Similarly, cheaper data packs have revolutionized the Indian market, people are more connected now than ever before.<br><br> 
IoT based solutions can provide the cushion of comfort with affordability. By using IoT enabled products in households, the occupants can make informed choices of optimally operating the energy consuming appliances and equipment. With the help of integrated advanced control and automation systems, energy consumption of devices can be limited. Utilities can take advantage of information exchange from these IoT based solutions to offset the peak load and conduct demand response programs through grid connected devices over smart integrated networks.<br><br> 
We believe that IoT based systems through demand side management in residential buildings will contribute in achieving India’s Nationally Determined Contributions (NDC) goals of limiting carbon emission intensity of the economy.<br><br> 
<b>Future of IoT in residential buildings</b><br> 
In India, residential buildings account for almost one-fourth of the total electricity consumption. This demand is increasing due to rapid growth in economy, improved living standards and high pace of urbanization. It is expected that by 2030, residential sector will become the highest energy consuming sector.<br><br> 
The projected electricity demand in residential sector is also driven by aspirations of occupants to have improved comfort (thermal, visual and air quality) and user-behaviour. With the technological development in IoT, the cost of integrating IoT enabled systems has become affordable and it is expected that the cost of such systems may further go down if they are pursued by large group of users.<br><br> 
Deity envisions IoT industry in India to word USD 15 billion by 2020. With smart city mission in place, increasing netizens and mushroom growth of home automation-based startups, this vision seems quite achievable.<br>
