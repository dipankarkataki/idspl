                    out of 115 districts.<br>
                    <img src="../images/blog4_4.jpeg" />
                    <br><br>
                    After acquiring permission from Additional District Magistrate (ADM), IDSPL did a ground
                    survey to find about the current situation and the findings were concerning. The district was
                    affected by tropic cyclones every year, whereas recent cyclone caused immense damage to the
                    infrastructure of the district. In the survey, major issues that were overlooked were electricity,
                    drinking water, proper sanitation in health and educational institutions. The schools as well
                    as, health institutions lacked basic amenities such as desk, benches, science & computer lab
                    equipment and medical equipment. The number of health institutions was also less; therefore,
                    people have to travel a large distance to get proper access to medication. For instance, people
                    from a certain village of Rayagada block had to travel around 65 km to go to a Community
                    health centre, that resulted in difficulty, especially for pregnant women and small children.
                    Following the survey, a meeting was held with the ADM and IDSPL decided to work in two
                    plain blocks and one tribal block of the district. The company decided to work on two areas;
                    Health and Education based on the resources available. IDSPL is working on various
                    strategies that will provide solutions against the alarming indicators present in the district;
                    thereby trying to improve its present ranking. 

