<p>The Paris Accord is an agreement aimed at combating climate change and accelerating and intensifying the actions and investments needed for a sustainable low carbon future. The goal of the Accord is to limit the global temperature increase to below 2oC (3.6 F) above pre-industrial levels by the year 2100. The best-case scenario hoped for is that the Accord will keep the increase to below 1.5oC (2.7 F). The agreement is also known as the 21st Conference of the Parties to the UN Framework Convention on Climate Change (UNFCCC).<br><br>

The UNFCCC is an international environmental treaty with the objective of “stabilizing greenhouse gas concentrations in the atmosphere at a level that would prevent dangerous anthropogenic interference with the climate system". The framework was designed to be non-binding in terms of limits on greenhouse gas emissions for individual countries and contains no enforcement mechanisms. It outlines "protocols" or "agreements" that are negotiated to specify further action towards the objective of the UNFCCC. The Paris Accord, held in December 2015, is its latest iteration. Countries that sign the Accord are encouraged to develop renewable energy sources and build infrastructure to mitigate the effects of global warming. The countries are also expected to report on their progress toward and plans for cutting greenhouse gas emissions every five years. It also requires developed countries to send $100 billion a year to developing countries starting in 2020.<br><br>

195 UNFCCC members have signed the agreement, and 174 have become party to it. The Paris Agreement is a replacement for the 2005 Kyoto Protocol. The Kyoto Protocol was not successful as it set internationally binding emission reduction targets, placing the onus on the developed countries under the principle of "common but differentiated responsibilities", stating that the current high levels of greenhouse gases (GHG) in the atmosphere are the result of more than 150 years of industrial activity by the developed nations.<br><br>

The key difference between the Kyoto Protocol and the Paris Accord is the willingness of the countries to participate in the Paris accord — which is a wholly voluntary agreement. In another first, both China and the US signed the accord. China is responsible for 30% of global emissions, and the US for 14%. On June 1, 2017, however, United States President Donald Trump announced that the US would withdraw from the 2015 Paris Agreement, stating that it would permanently undermine US domestic economy. The decision was met with widespread criticism, internally and externally, leading several state governors to form the United States Climate Alliance which pledged to continue to adhere to and advance the Paris Agreement.<br><br>

<b>India’s commitments at the Paris Accord</b><br><br>

The Paris Agreement was ratified by India on 2 October 2016. The main elements of India’s Intended Nationally Determined Contribution (INDC) includes:<br><br>

•	To reduce the emissions intensity of GDP by 33%–35% by 2030 below 2005 levels;<br>
•	To increase the share of non-fossil-based energy resources to 40% of installed electric power capacity by 2030, with help of transfer of technology and low-cost international finance including from Green Climate Fund (GCF);<br>
•	To create an additional carbon sink of 2.5–3 GtCO2e through additional forest and tree cover by 2030.<br><br>

  <b>National Action Plan on Climate Change</b><br><br>

     In order to formulate a plan of action to address climate change, the Government of India created the National Action Plan on Climate Change (NAPCC) in 2008. The action plan identified eight national missions running through 2017 to 2022 highlighting India’s most pressing climate concerns and outlines independent targets for emission mitigation within the different sectors.<br><br>

     Eight National Missions form the core of the NAPCC:<br><br>

•	National Solar Mission<br>
•	National Mission for Enhanced Energy Efficiency<br>
•	National Mission on Sustainable Habitat<br>
•	National Water Mission<br>
•	National Mission for Sustaining the Himalayan Ecosystem<br>
•	National Mission for a Green India<br>
•	National Mission for Sustainable Agriculture<br>
•	National Mission on Strategic Knowledge for Climate Change<br><br>

A range of policy instruments were identified to create an impetus for mitigation in all target economic sectors. Some of the policy instruments identified to address climate change mitigation are price instruments (such as coal cess and feed-in-tariff), regulatory instruments (legislations), voluntary instruments (awareness-building programmes and labelling of appliances), and targeted research and development (R&D) instruments to support the different sectors.<br><br>.

<b>Mitigation policies</b><br><br>

In 2010, the Government of India created the National Clean Energy Fund (NCEF) to finance and promote clean energy initiatives and funding research in the area of clean energy in the country. The fund is built by levying a cess of INR 50 (subsequently increased to INR 100 in 2014 and INR 200 in 2015) per tonne of coal produced domestically or imported.<br><br>

The NCEF is financing innovative schemes like the installation of solar photovoltaic (SPV) lights and small capacity lights, installation of SPV water pumping as well as other mission projects under the NAPCC and projects relating to R&D to replace existing technologies with more environment-friendly ones under National Mission on Strategic Knowledge for Climate Change (NMSKCC).<br><br>

In November 2010, the Renewable Energy Certificate (REC) mechanism was developed to help initiate a move towards renewable power sources in electricity generation. Under the Mechanism, RECs will be issued to the RE generators for 1 MWh of electricity injected into the grid from renewable energy sources. Grid-connected RE Technologies with a minimum capacity of 250 KW would be eligible under this scheme. India aims to increase its total installed renewable energy capacity to 175,000 MW by 2022. 100,000 MW of this would come from solar power, 60,000 MW from wind energy, 10,000 MW from small hydropower, and 5,000 MW from biomass-based power projects.<br><br>

In December 2012, the Government of India constituted an Expert Committee for drafting the Auto Fuel Vision and Policy-2025 for the country (MoPNG 2014). By the end of XII FYP, the Expert Committee has recommended a roadmap for rolling out Bharat Stage-IV (BS-IV), the equivalent of Euro-IV, by 2017 and BS-V (Euro-V) auto fuels by 2020 in the entire country.<br><br>

During 2013-14, the Climate Change Action Programme (CCAP) was launched to support actions by the central and state governments and other key stakeholders in areas of climate change. CCAP is envisioned to launch studies and projects to address the challenge of climate change in all dimensions and would also augment activities including Coordination of National and State Action Plans on Climate Change and setting up of an autonomous body to conduct analytical studies on scientific, environmental, economic development and technological issues related to climate change.<br><br>

In 2013, the government launched the National Electric Mobility Mission for 2020 to promote the growth of domestic manufacturing capabilities in electric mobility in the country. As per the government projections, 6–7 million units of new vehicle sales of the full range of electric vehicles, leading to resultant liquid fuel savings of 2.2–2.5 million tonnes and substantial lowering of vehicular emissions by 1.3% to 1.5% can be achieved as compared to a baseline scenario in 2020.<br><br>

<b>Success so far</b><br><br>

In its report to the UNFCCC in 2019, India stated that emission intensity had come down by 21 per cent. India is poised to meet the 2030 emission intensity commitment with ease. An analysis by US-based Institute for Energy Economics and Financial Analysis has stated that the figure indicates that India could meet its target a decade early.<br><br>

India also committed to ensuring that 40 percent of its installed power capacity is from non-fossil sources by 2030. As non-fossil sources account for about 37 per cent of India’s power capacity, according to the Central Electricity Authority, this target is easily achievable.<br><br>

India’s final key pledge at Paris was the creation of an additional carbon sink equivalent to 2.5–3 billion tonnes CO2 by 2030 through additional forest and tree cover. Progress on this front has been far from expected. The Green India Mission is severely underfunded and has been regularly missing its annual targets.<br><br>

Overall, India seems to have fared much better than most other developing countries and is well on its way to meeting two of the three commitments made at the Paris Accord. However, some more initiatives on increasing the green cover would go a long way. 
