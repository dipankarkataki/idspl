IoT marks a paradigm shift in the world of technology. In fact, one might say that the incorporation of technology into life that began with the Industrial revolution comes to an end as we move into the next stage: integration. It would be no exaggeration to say that the level of technological immersion that we do not give a second thought to today would have been unthinkable as recently as twenty years ago. The world at large has been in a constant state of flux, with updates coming in almost as soon as a piece of tech hits the market. Almost every chore in our personal lives that needed physical capital has been automated to some extent, if not entirely. <br><br>
IoT is all about integration. It integrates all kinds of tech into a network in order to facilitate holistic management. In fact, the Internet of Things is not just about computing devices and digital machines, it also incorporates mechanical devices, objects, and even animals and people! IoT helps streamline complex systems with minimal human intervention and optimise usage. We have already discussed IoT in a previous article. In this article, we look at ways in which IoT can be used to automate and optimise existing building systems.<br><br> 
<b>Water Management</b><br><br>
A third of the global population is currently suffering from some form of water scarcity. By 2030, it is expected to affect half the world. This makes it mandatory that we start checking our water usage, and eliminate waste as much as possible. IoT holds immense promise in this domain.<br><br>
<img src="../images/iotwater.jpg" /><br><br>
<b>Conservation</b><br>
IoT can be used to sense, regulate and collect usage data on water levels in reservoirs and water tanks. It will then be possible to calculate water usage levels throughout the year to optimise usage. If weather sensors are incorporated, it will also make it possible to prepare for spikes in usage.<br><br> 
<b>	Management </b><br> 
Water management involves monitoring water levels, leakages and water quality. With the help of pressure sensors, leakage of any kind can be immediately detected. In fact, it will be possible to set up the system in such a way that the moment a leak is detected, water supply to that section can be shut off till it is repaired. When employed for wastewater management, IoT can minimize human intervention, and check seepages and leaks that can cause pollution.<br><br> 
<b>Quality Testing and Analysis</b><br>
IoT can be used to measure and monitor water quality. Sensors can be used to detect levels of Total Dissolved Solids (TDS), bacteria, chlorine, etc. This will ensure that the water is fit for consumption and use, and significantly reduce chances of disease outbreaks.<br><br>
<b>Workspace Management</b><br>
More than half the world’s companies are expected to start using IoT by next year. IoT can significantly boost performance by increasing efficiency and reducing costs. By collecting data on office space usage, for instance, IoT can predict when a company will need larger premises. It can help decide if a new company can adopt a work from home ethic to prevent additional costs without compromising on productivity. By monitoring usage, the company can schedule maintenance on devices or machines in such a way as to minimize interruptions to workflow and also prevent breakdowns.<br><br>
<img src="../images/iotwater2.jpg" /><br><br>
This means printers will no longer run out of ink at a critical juncture, the temperature in the office will no longer alternate between freezing and sweltering, and smart fridges – if a company employs them – will always be restocked!<br><br>
<b>Cleaning and maintenance</b><br>
A necessary part of Facility Management is scheduling cleaning and maintenance at specific times. Obviously, this does not reflect real-time needs: cleaning and maintenance are done irrespective of whether there is any need. IoT can help us develop a dynamic schedule based on use that can bring down costs drastically.<br><br>
<b>HVACs</b><br>
IoT can be employed to collect data on a range of environmental components including light, temperature, humidity and carbon dioxide levels. Using the data, relevant systems can be turned on or off to maintain optimal levels. Using data collected over time, trends can be identified, which can then inform building design.<br><br>
<b>Power management</b><br>
Through the use of sensors, the process of switching on the lights, ACs, projectors, etc. can also be automated which can save massive amounts of power, and reduce the company’s carbon footprint. Smart meters can be used to detect loss of electricity and monitor levels. Motion detectors can also be used with smart cameras to start recording only when there is movement.<br><br>
<b>Stocks and supplies</b><br>
Usage of restrooms and supplies can be monitored, making supplies management more efficient by automated scheduling requisition of supplies. Sensors in restrooms can also help in water management and prompt maintenance of devices.<br><br>
<b>Safety and Security Systems</b><br>
IoT in facilities management can be used for the remote monitoring of smoke detectors, fire alarms and other safety systems. It can provide real-time information about a crisis by collecting data from heat detectors and other safety devices, and even prevent it by alerting management to potential overloads. Most significantly, in the event of an emergency, real-life tracking of employees can be initiated to ensure the evacuation of every individual.<br><br>
Every single smart controller that is installed, automates a large number of processes, and the benefits expand exponentially as these controllers are used in tandem to create smart grids. The ability of smart systems to integrate and streamline complex chains is bound only by the imagination of the users, and as such, the potential of IoT is virtually limitless.
