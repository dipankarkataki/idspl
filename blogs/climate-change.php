                  <p>   Such reports give a sense of hope that the nature is healing itself. However, such phenomenon is temporary and short-lived. The reality is that the natural resources have already been exploited by the humans beyond a repair. The widespread catastrophe of climate change will lead to unimaginable losses, both in terms of direct loss of lives attributed to climate change and indirect impacts of the same.<br><br>
                     The most obvious and visible effect of the climate change is the rise in erratic weather patterns. One of the most recent phenomena has been the increasing frequency in the number of cyclones in the Arabian Sea. The year 2019 saw a record 5 cyclones in the region. This has been attributed to the unusual warming of the Indian Ocean waters. To put things into perspective, the last time this region saw these many cyclones was way back in the year 1902. The monsoon season in the year 2019 too, can be attributed to the climate change and its impacts. The official date for the end of the season in India is 30th September. However, in 2019, the monsoon started to recede in second week of October. Over 1600 people were documented as killed in what was termed as the heaviest monsoon in last 25 years. These numbers are expected to rise in the seasons to come as the unprecedented nature of the monsoon is going to increase.<br><br>
                    Climate change has a serious implication on the food security as well. Locusts are an annual phenomenon in parts of Rajasthan but this year the swarms came into the country as far as districts in Bihar and Maharashtra. This has been, in part, attributed to the unusual weather patterns in the months of March and April which saw high rainfall. The locusts came soon after. These insects can cause serious implications as far as food security is concerned. Apart from the locusts, the weather patterns in general have been changing, much of which can be attributed to the climate change, making drought and flood a regular thing in the areas which never experienced them . Some of the most recent extreme flooding events have been in the states of Assam, Bihar, West Bengal, Karnataka, Maharashtra, and Eastern Uttar Pradesh. The floods in Assam, Bihar and Eastern Uttar Pradesh have been especially devastating. These have led to a massive surge in the food losses. Further, the projected increases in temperatures, changes in precipitation patterns, changes in extreme weather events, and reductions in water availability may all result in reduced agricultural productivity.<br><br>
                     As per a WHO, between the years 2030 and 2050, climate change is expected to cause approximately 250,000 additional deaths per year, from malnutrition, malaria, diarrhea, and heat stress. The direct damage costs to health (i.e. excluding costs in health-determining sectors such as agriculture and water and sanitation), is estimated to be between USD 2-4 billion/year by 2030.<br><br>
                     The rise in sea levels is something that everyone has heard of before. The effects of this rise are not experienced by many though, for now. The situation is grim for many countries around the world. Closer to home, Bangladesh is under serious threat due to this rise with two-thirds of the country being less than 5 metres above the sea level. By 2050, Bangladesh may lose approximately 11% of its land to the Bay of Bengal. The problem is compounded by the fact that the country is one of the most densely populated ones in the world. Other coastal cities including Mumbai, Chennai, Bangkok, and Shanghai will have parts of them submerged under water too by 2050. One of the villages, Bedono, on the Northern Coast of the Java Island of Indonesia has seen the coastline shift 5 Km inland, completely submerging 2000 hectares of the land. More than 500 families have been forced to evacuate the place since the early 1990s and the problem continues to aggravate to this day. </p>
