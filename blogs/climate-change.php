                    <p>On the morning of 29th April this year, residents of Saharanpur, a quiet town in the plains of Western Uttar Pradesh, were in for a surprise. The residents of this little-known town could get a clear view of the peaks of Gangotri, situated at an aerial distance of at least 200 km, from their rooftops. Old timers believed that this is a first in almost 3 decades. Similar reports came in from the plains of Punjab and Bihar.</p><br>
